# Create a list containing the number day for the next 5 days
day_date = [2, 3, 4, 5, 6]

# Assign the 5 letters from the list to variables representing each day
thu, fri, mon, tue, wed = tuple(day_date)

# Print each of the variables to see their values
print("Thursday:", thu)
print("Friday:", fri)
print("Monday:", mon)
print("Tuesday:", tue)
print("Wednesday:", wed)
